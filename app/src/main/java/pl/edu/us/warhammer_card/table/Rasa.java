package pl.edu.us.warhammer_card.table;

public class Rasa {
    int id;
    String name;
    int wartoscMin;
    int WartoscMax;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getWartoscMin() {
        return wartoscMin;
    }

    public void setWartoscMin(int wartoscMin) {
        this.wartoscMin = wartoscMin;
    }

    public int getWartoscMax() {
        return WartoscMax;
    }

    public void setWartoscMax(int wartoscMax) {
        WartoscMax = wartoscMax;
    }
}
